    public DepositConfirm(DepositC d) {
        initComponents();   
        depositFrom = d;
        String cardno = depositFrom.getcardno().getText();
        String cardname = depositFrom.getcardname().getText();
        try{
            Connection con = MySqlConnect.ConnectDB(); 
            String query = "select * from acctinfo where cardNo = ? and acctName = ?" ;
                          
            PreparedStatement st = con.prepareStatement(query);
            st.setString(1, cardno);
            st.setString(2, cardname);
            ResultSet result  = st.executeQuery();
          
            if(result.next() ){
                accountNumTF.setText(result.getString("acctNo"));
                acctNameTF.setText(result.getString("acctName"));
                balanceTF.setText(result.getString("balance"));                
                cardNumTF.setText(result.getString("cardNo"));
                amtTF.setText(depositFrom.getamt().getText());
                currencyTF.setSelectedItem(depositFrom.getcurrency().getSelectedItem());
                password = result.getString("cardPassword");
            }
            else{
                 JOptionPane.showMessageDialog(null, "There is no eligible record, please check!!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Research failed! \n " + e);
        }
    }

