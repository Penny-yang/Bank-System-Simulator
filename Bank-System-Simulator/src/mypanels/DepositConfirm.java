    private void enterButnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButnActionPerformed
          String k = String.valueOf(passwordTF.getPassword());
        if(password.equals(k)){
         try{
            if((balanceTF.getText()).equals("")) {
             money = Double.parseDouble(amtTF.getText());
           }
            else{         
            money = Double.parseDouble(amtTF.getText())  + Double.parseDouble(balanceTF.getText());
           }
            
            String moneyresult = String.valueOf(money);
            Connection con = MySqlConnect.ConnectDB(); 
            String query = "update acctinfo set balance = ?,currency = ? where cardNo = ? ";
            PreparedStatement st = con.prepareStatement(query);
            st.setString(1,moneyresult);
            st.setString(2,currencyTF.getSelectedItem().toString());
            st.setString(3,cardNumTF.getText());
            int result  = st.executeUpdate();
          
            if(result > 0 ){
                JOptionPane.showMessageDialog(null, "Transaction is successful !!");
                this.setVisible(false);
                
            }
            else{
                 JOptionPane.showMessageDialog(null, "Transaction Failed !!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Research failed! \n " + e);
        }
     }else{
         JOptionPane.showMessageDialog(null, "Password is NOT correct!!");
     }
    }//GEN-LAST:event_enterButnActionPerformed

